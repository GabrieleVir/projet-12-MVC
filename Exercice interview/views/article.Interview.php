		<p>Voici donc l'interview faites par écrit avec Juan :</p>
		<!--About you questions starts-->
		<h3 id="about_you">Peux-tu te présenter brièvement et où as-tu travaillé ?</h3>
		<blockquote>Je suis autodidacte en programmation depuis mes 17 ans. J'ai 25 ans. Je suis freelance en web dev et IoT et je commence en tant que formateur chez Becode. J'ai travaillé chez PAF Design en tant que Web dev back et front. Ma fonction premiere etait de creer des sites web et applications IOS mais aussi de debuger. J'y ai travaillé 3 mois. J'avais d'autres ambitions.</blockquote>
		<!--About you questions ends-->

		<!--Motivation questions starts-->
		<h3 id="motivations">Qu'est-ce qui t'as donné envie de travailler dans le web-development ?</h3>
		<blockquote>J'ai commencé par apprendre le HTML CSS JS pour faire un site que mon frère devait faire pour un client avec un 3000 euros à la clé... à 17 ans on ne dit pas non pour un petit argent de poche. Du coup j'ai étudié et apris ce dont j'avais besoin pour le faire. Depuis j'y ai pris goût. Je faisais des trucs dégeux au debut mais à force de pratiquer... c'est comme le vélo on devient bon.</blockquote>

		<h3>Comment as-tu réussi à obtenir du travail dans ce domaine ?</h3>
		<blockquote>Comment j'ai eu du travail et bah je l'ai créé. J'ai fais mon site et j'ai mis des annonces dans quefaire etc.. au début, rien... mais qui n'abandonne pas, réussit toujours... J'ai postulé à une annonce chez Paf. Je me suis dit: "Ce poste est pour moi.".<br> Je leur ai envoyé un mail sans cv en leur proposant de passer leur présenter ce que je sais faire avec mon pc.</blockquote>
		<!--Motivation questions ends-->

		<!--Qualités questions starts-->
		<h3 id="qualites">Quelles sont les qualités personnelles nécessaires pour exercer ce métier selon toi ?</h3>
		<blockquote>Je dirais seulement d'en vouloir, de vouloir apprendre, savoir aller chercher les infos et se debrouiller.<br> Petit à petit on developpe une façon de penser qui rend plus facile la compréhension du code.</blockquote>

		<h3>Peux-tu préciser à quelles technologies tu fais appel au quotidien ?</h3>
		<blockquote>Il y en a beaucoup.. je m'adapte à la demande du client. Je ne fais plus beaucoup de web development mais des solutions IoT. Je dois connaitre une <strong>quinzaine</strong> de languages actuellement. J'écoute la demande, je regarde ce qu'il faut pour le faire, j'apprends, je dev.</blockquote>

		<h3>Qu’est-ce qui te plaît le plus dans ton métier et pourquoi ?</h3>
		<blockquote>Les <strong>possibilités infinies</strong> de ce qu'on peut faire. Et la <strong>réflexion</strong> qu'il y a derrière chaque projet. J'aime avoir des <strong>défis</strong>.</blockquote>
		<!--Qualités questions ends-->

		<!--contraintes questions starts-->
		<h3 id="contraintes">Y a-t-il des conditions de travail spécifiques ? Des contraintes physiques ?</h3>
		<blockquote>No pas vraiment juste manger beaucoup de fruits</blockquote>

		<h3>Quels sont les problèmes auxquels tu peux être confronté ?</h3>
		<blockquote>Comme dit plus haut, j'aime les défis: je ne vois pas ça comme des problèmes.<br>Mais je dirais que parfois tu peux manquer de temps pour rendre un travail à temps...
		</blockquote>
		<!--contraintes questions ends-->

		<!--Market questions starts-->
		<h3 id="market">Quelles sont les évolutions possibles selon toi ?</h3>
		<blockquote>En suivant mon parcours et bien on fait des choses de plus en plus complexes et plus grandes. <br><em>Je ne bosse plus pour une boite mais avec les boites</em> (Clearchannel, JCdecaux, etc)<br> On crée des solutions à leurs problèmes. En IoT pour ma part.</blockquote>
		
		<h3>Quel est l'état du marché de l'emploi pour ce métier ?</h3>
		<blockquote>C'est un metier qui est <strong>beaucoup demandé partout dans le monde.</strong><br> Il y a une tonne de postes disponibles.
		Je recois au moins 10 offres par jour</blockquote>

		<h3>Existe-t-il des difficultés de recrutement dans ton métier ?</h3>
		<blockquote>Comme dans tout recrutement, le recruteur a besoin d'être impressionné... faut savoir <strong>se mettre en avant</strong></blockquote>

		<h3>As-tu déjà été démarché par un recruteur ? </h3>
		<blockquote>Oui, sur Linkedin.</blockquote>
		<!--Market questions ends-->

		<!--Conseils questions starts-->
		<h3 id='conseils'>Y a-t-il des personnes à qui tu déconseillerais de faire ce métier ?</h3>
		<blockquote>Je le souhaite à <strong>tout le monde qui en veut</strong>, c'est un domaine sans limite.</blockquote>

		<h3>Quels conseils me donnerais-tu ?</h3>
		<blockquote><strong>Crée des choses ou innove</strong>, je veux dire par là: <strong>refait des choses qui existent déjà.</strong> Des choses que tu peux présenter à tes entretiens. <strong><br>Cela permet de fonder ton profil</strong> en tant que developper mais aussi pour toi <strong>de savoir vers quoi tu veux aller.</strong><br>
		HTML CSS JS n'est qu'un debut qui ouvre deja beaucoup mais tu peux aller bien plus deep	si tu le veux</blockquote>
		<!--Conseils questions ends-->
		
